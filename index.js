puzzle = [
    [5,0,0,9,1,3,7,2,0],
    [3,0,0,0,8,0,5,0,9],
    [0,9,0,2,5,0,0,8,0],
    [6,8,0,4,7,0,2,3,0],
    [0,0,9,5,0,0,4,6,0],
    [7,0,4,0,0,0,0,0,5],
    [0,2,0,0,0,0,0,0,0],
    [4,0,0,8,9,1,6,0,0],
    [8,5,0,7,2,0,0,0,3]
]

function loadPuzzle() {
    for(r=0; r<9; r++) {
        for(c=0; c<9; c++) {
            document
                .querySelector(`[row='${r}'][col='${c}']`)
                .setAttribute('value', puzzle[r][c])
        }
    }
}

loadPuzzle()